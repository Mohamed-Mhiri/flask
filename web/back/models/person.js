class Person {
    constructor(id, name, lastName, gender, age, email, password, phone) {
      this.id = id;
      this.name = name;
      this.lastName = lastName;
      this.gender = gender;
      this.age = age;
      this.email = email;
      this.password = password;
      this.phone = phone;
    }
  }
  
  module.exports = Person;
  